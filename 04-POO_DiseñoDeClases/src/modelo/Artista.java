package modelo;

public class Artista {

	public void dibuja(String s) {
		System.out.println(s);
	}
	
	public void dibuja(int i) {
		System.out.println(i);
	}
	
	public void dibuja(double d) {
		System.out.println(d);
	}
	
	public void dibuja(int i, double d) {
		System.out.println(i);
		System.out.println(d);
	}

}
